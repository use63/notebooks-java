    private void registerBtnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnRegisterActionPerformed
        // TODO add your handling code here:
        // fungsi register
        
        try {
            st = cn.createStatement();

            if (registerTxtNama.getText().equals("") || registerTxtEmail.getText().equals("") || registerTxtPassword.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Isi semua form!");
                return;
            }

            if ("Register".equals(registerBtnRegister.getText())) {
                String cek = "SELECT * FROM users WHERE email = ?";
                try (java.sql.PreparedStatement preparedStatement = cn.prepareStatement(cek)) {
                    preparedStatement.setString(1, registerTxtEmail.getText());
                    ResultSet rs = preparedStatement.executeQuery();

                    if (rs.next()) {
                        JOptionPane.showMessageDialog(null, "Ups... email sudah terdaftar.");
                    } else {
                        String insertSql = "INSERT INTO users (name, email, password) VALUES (?, ?, ?)";
                        try (java.sql.PreparedStatement insertStatement = cn.prepareStatement(insertSql)) {
                            insertStatement.setString(1, registerTxtNama.getText());
                            insertStatement.setString(2, registerTxtEmail.getText());
                            insertStatement.setString(3, registerTxtPassword.getText());

                            int rowsAffected = insertStatement.executeUpdate();

                            if (rowsAffected > 0) {
                                cleanRegister();
                                layerLogin();
                                JOptionPane.showMessageDialog(null, "Berhasil membuat akun.\nSilahkan login.");
                            } else {
                                JOptionPane.showMessageDialog(null, "Gagal membuat akun.");
                            }
                        }
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }

    }//GEN-LAST:event_registerBtnRegisterActionPerformed

